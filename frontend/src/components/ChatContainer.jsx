// ChatContainer.jsx
import { useChatStore } from "../store/useChatStore";
import { useAuthStore } from "../store/useAuthStore";
import { useEffect, useRef } from "react";

import ChatHeader from "./ChatHeader";
import MessageInput from "./MessageInput";
import MessageSkeleton from "./skeletons/MessageSkeleton";
import { formatMessageTime } from "../lib/utils";

const ChatContainer = ({ onBack }) => {
  const {
    messages,
    getMessages,
    isMessagesLoading,
    selectedUser,
    subscribeToMessages,
    unsubscribeFromMessages,
  } = useChatStore();

  const { authUser } = useAuthStore();
  const messageEndRef = useRef(null);

  useEffect(() => {
    if (!selectedUser?._id) return;
    getMessages(selectedUser._id);
    subscribeToMessages();
    return () => unsubscribeFromMessages();
  }, [selectedUser?._id, getMessages, subscribeToMessages, unsubscribeFromMessages]);

  useEffect(() => {
    if (messageEndRef.current) {
      messageEndRef.current.scrollIntoView({ behavior: "smooth" });
    }
  }, [messages]);

  if (isMessagesLoading) {
    return (
      <div className="flex flex-col h-full overflow-hidden">
        <ChatHeader onBack={onBack} />
        <MessageSkeleton />
        <MessageInput />
      </div>
    );
  }

  return (
    <div className="flex flex-col h-full overflow-hidden">
      {/* Header */}
      <ChatHeader onBack={onBack} />

      {/* Messages area */}
      <div className="flex-1 overflow-y-auto p-4 space-y-4">
        {messages.map((msg) => (
          <div
            key={msg._id}
            className={`chat ${msg.senderId === authUser._id ? "chat-end" : "chat-start"}`}
          >
            <div className="chat-image avatar">
              <div className="size-10 rounded-full border">
                <img
                  src={
                    msg.senderId === authUser._id
                      ? authUser.profilePic || "/avatar.png"
                      : selectedUser.profilePic || "/avatar.png"
                  }
                  alt="profile pic"
                />
              </div>
            </div>

            <div className="chat-header mb-1">
              <time className="text-xs opacity-50 ml-1">{formatMessageTime(msg.createdAt)}</time>
            </div>

            <div
              className="
                chat-bubble flex flex-col
                max-w-[320px]
                sm:max-w-[250px]
                md:max-w-[280px]
                lg:max-w-[340px]
                xl:max-w-[380px]
                max-[450px]:max-w-[180px]
                max-[360px]:max-w-[160px]
                max-[320px]:max-w-[150px]
              "
            >
              {msg.image && (
                <img
                  src={msg.image}
                  alt="Attachment"
                  className="
                    rounded-md mb-2
                    max-w-[250px]
                    sm:max-w-[200px]
                    md:max-w-[180px]
                    max-[450px]:max-w-[140px]
                    max-[360px]:max-w-[120px]
                    max-[320px]:max-w-[110px]
                  "
                />
              )}
              {msg.text && <p>{msg.text}</p>}
            </div>

            <div ref={messageEndRef} />
          </div>
        ))}
      </div>

      {/* Input (sticky bottom) */}
      <MessageInput />
    </div>
  );
};

export default ChatContainer;



